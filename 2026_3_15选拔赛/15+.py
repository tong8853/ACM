# 1. 导入必要的库
import bisect

# 2. 读取输入
n = int(input())  # 读取序列的总长度 N
seq = list(map(int, input().split())) 
# 这行代码的作用是：
# - input().split(): 读取一行字符串，并按空格分割成多个字符串片段。
# - map(int, ...): 将这些字符串片段一个个转换成整数。
# - list(...): 将转换后的整数序列收集到一个 Python 列表 (list) 中。
# 例如，如果输入是 "8 4 2 5 3 9 1 6 7"，那么 seq 就变成了 [8, 4, 2, 5, 3, 9, 1, 6, 7]

# 3. 初始化一个空列表，用于模拟维护 LIS 的结构
lis = []

# 4. 遍历输入序列中的每一个数字
for x in seq:
    # 5. 使用 bisect 模块进行二分查找
    pos = bisect.bisect_left(lis, x)
    # bisect.bisect_left(a, x) 在有序列表 a 中查找 x 应该插入的位置，
    # 以保持列表 a 的有序性。它返回最左边的符合条件的索引。
    # 例如，如果 lis = [2, 5, 7]，当 x=3 时，pos 将是 1，因为 3 应该插在 2 和 5 之间。

    # 6. 根据查找到的位置进行更新
    if pos == len(lis):
        # 这个条件意味着，x 比 lis 中的任何一个数都要大。
        # 也就是说，我们可以把 x 接在当前最长递增子序列的后面，形成一个更长的序列。
        # 所以，我们将 x 添加到 lis 的末尾。
        lis.append(x)
    else:   
        # 这个条件意味着，x 不比 lis 中的所有数都大。
        # pos 是 x 应该插入的位置。我们不插入，而是直接替换掉那个位置上的数。
        # 这个操作的意义是：我们找到了一个“更优”的、长度相同但结尾更小的递增子序列。
        # 这样做是为了为未来可能接到更长的序列做准备。
        lis[pos] = x

# 7. 输出结果
print(len(lis))
# 循环结束后，lis 列表的长度就是原始序列的最长递增子序列的长度。
# 根据我们之前的讨论，这个长度就是调度问题所需的最少轨道数。